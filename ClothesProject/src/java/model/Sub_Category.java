package model;

/**
 *
 * @author Pham Thang
 */
public class Sub_Category {
    private int subID;
    private Category category;
    private String name;

    public Sub_Category() {
    }

    public Sub_Category(int subID, Category category, String name) {
        this.subID = subID;
        this.category = category;
        this.name = name;
    }

    public int getSubID() {
        return subID;
    }

    public void setSubID(int subID) {
        this.subID = subID;
    }

    public Category getCategory() {
        return category;
    }

    public void setCategory(Category category) {
        this.category = category;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
