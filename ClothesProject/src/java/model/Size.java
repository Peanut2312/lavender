package model;

/**
 *
 * @author Pham Thang
 */
public class Size {
    private int sizeID;
    private String size;
    private Category category;

    public Size() {
    }

    public Size(int sizeID, String size, Category category) {
        this.sizeID = sizeID;
        this.size = size;
        this.category = category;
    }

    public int getSizeID() {
        return sizeID;
    }

    public void setSizeID(int sizeID) {
        this.sizeID = sizeID;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public Category getCategory() {
        return category;
    }

    public void setCategory(Category category) {
        this.category = category;
    }
    
}
