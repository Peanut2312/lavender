    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DAO d = new DAO();

        List<Product> newProduct = d.getAllNewProduct();
        request.setAttribute("newProduct", newProduct);
        List<Category> listCate = d.getAllCategory();
        request.setAttribute("listCate", listCate);
        List<Product> listBuy = new ArrayList<>();
        Cookie[] arr = request.getCookies();
        String txt = "";
        if (arr != null) {
            for (Cookie o : arr) {
                if (o.getName().equals("cart")) {
                    txt += o.getValue();
                }
            }
        }
        Cart cart = new Cart(txt);
        List<Item> listItem = cart.getItems();
        int n;

        if (listItem != null) {
            n = listItem.size();
        } else {
            n = 0;
        }
        //Sale
        SaleDAO sd = new SaleDAO();
        Sale sale = sd.getSaleByTime();
        if (sale != null) {
            if (!sale.isApply()) {
                sd.apply(sale);
                sd.updatePrice(sale);
            }
            
            if (sd.checkAvailable(sale)==false) {
                sd.backPrice(sale);
//                sd.deleteSale(sale);
            }
        }
        List<Product> listNew=d.getNewProduct();
        List<Product> listView=d.getHotProduct();
        request.setAttribute("listNew", listNew);
        request.setAttribute("listView", listView);
        request.setAttribute("listOld", d.getOldProduct());
        request.setAttribute("sale", sale);
        request.setAttribute("sizeCart", n);
        request.getRequestDispatcher("home.jsp").forward(request, response);
    }

